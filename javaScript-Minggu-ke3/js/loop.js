// WHILE
let mulai = 1;
let selesai = 10;

while (mulai <= selesai) {
    console.log(mulai);

    mulai++;
}

// DO WHILE
let start = 100;
let finish = 110;

do {
    console.log(start);
    start++;
} while (start <= finish);

// FOR LOOP 
let awal = 1001;
let akhir = 1010;

for (awal; awal <= akhir; awal++) {
    console.log(awal);
}

console.log("-----------------");

for(i = 1; i <= 10; i = i+2) {
    console.log(i);
}
